package Basics;

public class caseexmpl {
    public static void main(String[] args) {
        int i = 0;
        switch (i){
            case 0:
            System.out.println("Hello world");
            case 1:
            System.out.println("Hello");
            case 2:
            System.out.println("aaryan");
            case 3:
            System.out.println("Hello again");
            default:
            System.out.println("Wrong Choice");
            break;
        }
    }
}